# This program demonstrates how to build a dictionary and how to call upon
# its contents.

person = {
  'first_name': 'tony',
  'last_name': 'stark',
  'age': 42,
  'city': 'new york city' 
}

print(person['first_name'].title())
print(person['last_name'].title())
print(person['age'])
print(person['city'].title())