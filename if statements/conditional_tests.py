# This program shows a few examples of conditional tests.

car = 'honda'
print("Is car.title() == 'Honda'? I predict True.")
print(car.title() == 'Honda')

fruit = 'tomato'
print("Is fruit == 'tomato'? I predict True.")
print(fruit == 'tomato')

pet = 'cat'
print("Is pet == 'cat'? I predict True.")
print(pet == 'cat')

name = 'mike'
print("Is name == 'Mike'? I predict False!")
print(name == 'Mike') #this is because Python is case sensitive