# This program uses a series of 'if' statements to check if certain items
# are on a list or not.

favorite_fruits = ['apple', 'banana', 'orange']

if 'apple' in favorite_fruits:
  print("Apples are great!")
if 'orange' in favorite_fruits:
  print("Oranges are great!")
if 'blueberry' in favorite_fruits:
  print("Blueberries are great!")
if 'kiwi' in favorite_fruits:
  print("Kiwis are great!")