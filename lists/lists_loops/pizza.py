# This program demonstrates how to use a simple 'for' loop with a list.
# A statement at the end shows how to code outside of a for loop

favorite_pizzas = ['pepperoni', 'deep dish', 'double cheese', 'mushroom']

for pizza in favorite_pizzas:
  print(f"I really like {pizza} pizza.")

print("\nI really love all types of pizza!")